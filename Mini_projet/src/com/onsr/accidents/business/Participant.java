package com.onsr.accidents.business;



public class Participant{
 
private   String individu;
private String situation;
private String sexe;
private int age;
private String metier;
private String date_permis;
private String ceinture;
private String casque;
private String cause;








public String getSexe() {
	return sexe;
}
public void setSexe(String sexe) {
	this.sexe = sexe;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public String getSituation() {
	return situation;
}
public void setSituation(String situation) {
	this.situation = situation;
}
public String getMetier() {
	return metier;
}
public void setMetier(String metier) {
	this.metier = metier;
}
public String getDate_permis() {
	return date_permis;
}
public void setDate_permis(String date_permis) {
	this.date_permis = date_permis;
}
public String getCeinture() {
	return ceinture;
}
public void setCeinture(String ceinture) {
	this.ceinture = ceinture;
}
public String getCasque() {
	return casque;
}
public void setCasque(String casque) {
	this.casque = casque;
}
public String getCause() {
	return cause;
}
public void setCause(String cause) {
	this.cause = cause;
}
public String getIndividu() {
	return individu;
}
public void setIndividu(String individu) {
	this.individu = individu;
}






}
